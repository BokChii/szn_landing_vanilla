/**
 * Writes js/config.js from environment variables (Vercel build / local CI).
 * Expected: SUPABASE_URL, SUPABASE_ANON_KEY
 * Optional: PREORDER_TABLE (default: pre_registrations)
 */
import { writeFileSync, mkdirSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const outDir = join(root, 'js');
const outPath = join(outDir, 'config.js');

const url = (process.env.SUPABASE_URL || '').trim();
const key = (process.env.SUPABASE_ANON_KEY || '').trim();
const table = (process.env.PREORDER_TABLE || 'pre_registrations').trim();

const isVercel = process.env.VERCEL === '1';
const isProduction = process.env.VERCEL_ENV === 'production';

if (isVercel && isProduction && (!url || !key)) {
    console.error(
        '[write-supabase-config] Production build requires SUPABASE_URL and SUPABASE_ANON_KEY in Vercel → Settings → Environment Variables.',
    );
    process.exit(1);
}

if (isVercel && !isProduction && (!url || !key)) {
    console.warn(
        '[write-supabase-config] Preview: SUPABASE_URL or SUPABASE_ANON_KEY missing — preorder will not work until both are set for this environment.',
    );
}

const content = `/* Generated by scripts/write-supabase-config.mjs (do not edit in CI) */
export const SUPABASE_URL = ${JSON.stringify(url)};
export const SUPABASE_ANON_KEY = ${JSON.stringify(key)};
export const PREORDER_TABLE = ${JSON.stringify(table)};
`;

mkdirSync(outDir, { recursive: true });
writeFileSync(outPath, content, 'utf8');
console.log('[write-supabase-config] Wrote', outPath, url ? '(URL ok)' : '(empty URL)');
